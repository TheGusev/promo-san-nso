export function getFAQByCategory(category: FAQCategory): FAQItem[] {
  return FAQ_ITEMS.filter((item) => item.category === category);
}

export function getPopularFAQ(limit = 6): FAQItem[] {
  return FAQ_ITEMS.filter((item) => item.isPopular).slice(0, limit);
}

export function searchFAQ(query: string): FAQItem[] {
  const lowerQuery = query.toLowerCase();
  return FAQ_ITEMS.filter(
    (item) =>
      item.question.toLowerCase().includes(lowerQuery) ||
      item.answer.toLowerCase().includes(lowerQuery) ||
      item.keywords.some((k) => k.toLowerCase().includes(lowerQuery))
  );
}

export function getFAQById(id: string): FAQItem | undefined {
  return FAQ_ITEMS.find((item) => item.id === id);
}

export function getRelatedFAQ(item: FAQItem, limit = 3): FAQItem[] {
  // Находим связанные вопросы по пересечению услуг, вредителей, объектов
  return FAQ_ITEMS.filter(
    (other) =>
      other.id !== item.id &&
      (other.category === item.category ||
        other.relatedServices?.some((s) => item.relatedServices?.includes(s)) ||
        other.relatedPests?.some((p) => item.relatedPests?.includes(p)))
  ).slice(0, limit);
}
