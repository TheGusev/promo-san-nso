// Функция для получения статей по категории
export function getArticlesByCategory(category: BlogTopic["category"]): BlogTopic[] {
  return BLOG_TOPICS.filter((topic) => topic.category === category);
}

// Функция для получения статьи по slug
export function getArticleBySlug(slug: string): BlogTopic | undefined {
  return BLOG_TOPICS.find((topic) => topic.slug === slug);
}

// Функция для получения связанных статей
export function getRelatedArticles(currentSlug: string, limit = 3): BlogTopic[] {
  const current = getArticleBySlug(currentSlug);
  if (!current) return [];

  // Сначала ищем статьи из списка relatedArticles
  const directlyRelated = current.relatedArticles
    .map((slug) => getArticleBySlug(slug))
    .filter((article): article is BlogTopic => article !== undefined);

  // Если не хватает, добавляем из той же категории
  if (directlyRelated.length < limit) {
    const sameCategory = BLOG_TOPICS.filter(
      (t) => t.category === current.category && t.slug !== currentSlug && !current.relatedArticles.includes(t.slug)
    );
    return [...directlyRelated, ...sameCategory].slice(0, limit);
  }

  return directlyRelated.slice(0, limit);
}

// Получить все категории с количеством статей
export function getCategoriesWithCount(): { category: keyof typeof BLOG_CATEGORIES; count: number }[] {
  const categories = Object.keys(BLOG_CATEGORIES) as (keyof typeof BLOG_CATEGORIES)[];
  return categories.map((category) => ({
    category,
    count: getArticlesByCategory(category).length,
  }));
}
